package ui

import "github.com/charmbracelet/lipgloss"

// Portfolio is the single source of truth for all content displayed in the TUI.
// To customise this portfolio, edit the fields below — no view code changes needed.
//
// ASCII art portrait: regenerate with
//
//	ascii-image-converter docs/assets/photo.jpg -W 40 -b -C
//
// Name art: regenerate with
//
//	figlet -f small "yourname"
type Config struct {
	// Portrait is the ASCII art shown in the left column.
	Portrait string

	// NameArt is the ASCII art name shown above the content area.
	NameArt string

	// RoleLines are the regular-weight bio lines shown in the About view.
	RoleLines []string

	// CurrentWork are the bold-highlighted lines shown in the About view.
	CurrentWork []string

	// Background are the muted lines (education, location) in the About view.
	Background []string

	// Creations is the list of projects shown in the Creations view.
	Creations []Creation

	// Facts are the bullet points shown in the Fun Facts view.
	Facts []string

	// Links are the contact entries shown in the Contact view.
	Links []Link

	// AccentColor controls the highlight color used throughout the TUI:
	// nav cursor, name art, footer keys, bullet points, and selected items.
	//
	// Value is a 256-color terminal code (as a string). Pick one:
	//
	//	"39"  – bright cyan-blue  (default)
	//	"51"  – electric cyan
	//	"82"  – bright green
	//	"205" – hot pink
	//	"220" – amber/gold
	//	"141" – soft purple
	//
	// Full palette: https://en.wikipedia.org/wiki/ANSI_escape_code#8-bit
	// Preview in terminal: for i in {0..255}; do printf "\e[38;5;${i}m %3d \e[0m" $i; done
	AccentColor string

	// DimColor is the pale version of the accent used for the active nav tab
	// when the cursor has moved elsewhere. Should be a darker shade of AccentColor.
	//
	//	"68"  – muted blue  (default, pairs with "39"/"51")
	//	"64"  – muted green (pairs with "82")
	//	"162" – muted pink  (pairs with "205")
	//	"136" – muted amber (pairs with "220")
	//	"97"  – muted purple (pairs with "141")
	DimColor string
}

// accent and dim are derived from Portfolio so styles can reference them at package level.
var (
	accent = lipgloss.Color(Portfolio.AccentColor)
	dim    = lipgloss.Color(Portfolio.DimColor)
)

// Creation is a single project entry.
type Creation struct {
	Name  string
	Stack string
	Desc  string
}

// Link is a labelled contact entry.
type Link struct {
	Label string
	Value string
}

